              <p><img class="alignright wp-image-17168" src="https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-1024x684.jpg" alt="" width="452" height="302" srcset="https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-1024x684.jpg 1024w, https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-300x200.jpg 300w, https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-768x513.jpg 768w, https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-610x407.jpg 610w" sizes="(max-width: 452px) 100vw, 452px" />Earlier this year, I started educating myself about something called the <strong>Infinite Banking Concept</strong>.</p>

              <p>There’s a lot that goes into it (not the type of thing I can explain in one paragraph), but in essence, the idea is to rely less on banks and third-party lenders for meeting your financing needs and instead, <strong>build your own banking system</strong> inside of a whole life insurance policy, where you’ll able to lend the money to yourself, and then pay the money back (with interest) to your policy.</p>

              <p>When you borrow money this way <em>and actually pay it back</em> (the same way if you were borrowing it from a conventional lender), you can ultimately keep all of the money you would've otherwise spent on interest by funneling it into your own banking system.</p>
                          
              <p><img class="alignleft wp-image-17168" src="https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-1024x684.jpg" alt="" width="452" height="302" srcset="https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-1024x684.jpg 1024w, https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-300x200.jpg 300w, https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-768x513.jpg 768w, https://retipster.com/wp-content/uploads/2018/09/rawpixel-741658-unsplash-610x407.jpg 610w" sizes="(max-width: 452px) 100vw, 452px" />While it sounds like a fairly straightforward concept, there are a lot of considerations that go into it (because in my opinion, it doesn’t necessarily make sense for every person, depending on their financial situation).</p>

              <p>In this interview, I talk with <a href="http://cashflowninja.com" target="_blank" rel="noopener"><strong>M.C. Laubscher</strong></a> &#8211; who is an expert in the Infinite Banking Concept. My goal with this conversation was to get more clarity on the pros and cons of this strategy and to figure out <em>what I wasn’t being told by other “financial advisors”</em> who were just trying to sell me a life insurance policy.</p>

              <p><em><strong>Disclaimer:</strong> I am not a financial advisor, so please, do not interpret this blog post or this podcast episode as financial advice. You should definitely talk with your own financial advisor to help you decide whether the Infinite Banking Concept is the right fit for your situation.</em></p>
              
